-module(time_lib).

%%%=======================STATEMENT====================
-description("time_lib").
-author("yhw").

%%%=======================EXPORT=======================
-export([now_second/0, now_millisecond/0]).

%%%=======================INCLUDE======================

%%%=======================DEFINE======================

%%%=======================RECORD=======================

%%%=======================TYPE=========================
%%-type my_type() :: atom() | integer().


%%%=================EXPORTED FUNCTIONS=================
%% ----------------------------------------------------
%% @doc  
%%        
%% @end
%% ----------------------------------------------------
now_millisecond() ->
    {M, S, MS} = os:timestamp(),
    M * 1000000000 + S * 1000 + MS div 1000.
%% ----------------------------------------------------
%% @doc
%%
%% @end
%% ----------------------------------------------------
now_second() ->
    {M, S, _} = os:timestamp(),
    M * 1000000 + S.


